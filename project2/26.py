import math


print("Square root of 16:", math.sqrt(16))  
print("Sine of 0:", math.sin(0))          
print("Cosine of pi:", math.cos(math.pi))   
print("Tangent of pi/4:", math.tan(math.pi/4))  
print("e raised to the power of 2:", math.exp(2))   
print("Logarithm base 10 of 100:", math.log10(100))  
print("Value of pi:", math.pi)        
print("Value of e (Euler's number):", math.e)  
print("Absolute value of -5:", math.fabs(-5)) 
print("Factorial of 5:", math.factorial(5))   
print("Greatest common divisor of 36 and 24:", math.gcd(36, 24))  
